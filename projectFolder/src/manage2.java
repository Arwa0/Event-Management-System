    private void showspActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_showspActionPerformed
        // TODO add your handling code here:
        
        try {
            String connect = Db.getUrl();
            Connection conn = DriverManager.getConnection(connect);
            Statement st = conn.createStatement();
            String sql = "select distinct odate from vdate where sp_id = " +d1.getSpId(showsp.getSelectedItem().toString());
            ResultSet res = st.executeQuery(sql);
            while (res.next()) {
                showdate.addItem(res.getDate("odate"));
            }
            st.close();
            conn.close();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_showspActionPerformed

