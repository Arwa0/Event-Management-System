    public manage2(String name) {
        initComponents();
        this.name = name ;
        try {
            String connect = Db.getUrl();
            Connection conn = DriverManager.getConnection(connect);
            Statement st = conn.createStatement();
            String sql = "select distinct name from users where role_id = 3 ";
            ResultSet res = st.executeQuery(sql);
            while (res.next()) {
                showsp.addItem(res.getString("name"));
            }
            showdate.addItem(d1.getEventDate(name));
            sql = "select distinct odate from vdate where sp_id = " +d1.getSpIdFromEventId(name);
            res = st.executeQuery(sql);
            while (res.next()) {
                showdate.addItem(res.getDate("odate"));
            }
            st.close();
            conn.close();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
        }
        String[] s = {name, d1.getEventLocation(name),d1.getSpname(name),d1.getEventDate(name)+"",
            (d1.getSpPrice(name)+d1.getPmPrice(name))+"" ,d1.getStatus(name)};
        DefaultTableModel tb1 = (DefaultTableModel) showinfo.getModel();
        tb1.addRow(s);
        showname.setText("");
        showlocation.setText("");
        spprice.setText(d1.getSpPrice(name)+"");
        pmprice.setText(d1.getPmPrice(name)+"");
        total.setText((d1.getSpPrice(name)+d1.getPmPrice(name))+"");

    }

